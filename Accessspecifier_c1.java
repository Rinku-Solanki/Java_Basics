package Programs;

public class Accessspecifier_c1 {
	public static void add()
	{
		int a=5;
			int b=2;
			System.out.println(a+b);
				
	}
	private static void sub()
	{
		int a=5;
			int b=2;
			System.out.println(a-b);
				
	}
	protected static void mul()
	{
		int a=5;
			int b=2;
			System.out.println(a*b);
				
	}
	 static void div()
	{
		int a=5;
			int b=2;
			System.out.println(a/b);
				
	}
	public static void main(String[] args) {
		add();
		sub();
		mul();
		div();

	}

}
